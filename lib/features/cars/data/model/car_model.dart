class CarModel {
  const CarModel({
    required this.name,
    required this.brand,
    required this.model,
    required this.year,
    required this.status,
    required this.condition,
    required this.price,
    required this.description,
  });

  final String name;
  final String brand;
  final String model;
  final int year;
  final String status;
  final String condition;
  final String price;
  final String description;
}
